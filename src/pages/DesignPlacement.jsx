// ─── Canvas utilities ─────────────────────────────────────────────────────────

function loadImgEl(src) {
  return new Promise((resolve, reject) => {
    const img = new Image()
    img.crossOrigin = 'anonymous'
    img.onload = () => resolve(img)
    img.onerror = () => {
      // retry without crossOrigin (some local/data URLs don't support it)
      const img2 = new Image()
      img2.onload = () => resolve(img2)
      img2.onerror = reject
      img2.src = src
    }
    img.src = src
  })
}

async function stampSizeOnImage(dataUrl, sizeText) {
  if (!sizeText || !sizeText.trim()) return dataUrl
  const img = await loadImgEl(dataUrl)
  const W = img.naturalWidth || img.width
  const H = img.naturalHeight || img.height
  const canvas = document.createElement('canvas')
  canvas.width = W; canvas.height = H
  const ctx = canvas.getContext('2d')
  ctx.drawImage(img, 0, 0)
  const fontSize = Math.round(W * 0.08)
  const text = sizeText.trim().toUpperCase()
  ctx.font = `bold ${fontSize}px Arial, sans-serif`
  const tw = ctx.measureText(text).width
  const pad = fontSize * 0.35
  const rx = pad, ry = H - fontSize - pad * 2
  ctx.fillStyle = 'rgba(0,0,0,0.62)'
  ctx.fillRect(rx - pad * 0.5, ry - pad * 0.5, tw + pad, fontSize + pad)
  ctx.fillStyle = '#ffffff'
  ctx.textBaseline = 'top'
  ctx.fillText(text, rx, ry)
  return canvas.toDataURL('image/png')
}

// Remove white/paper background.
// Pass 1: edge-seeded BFS removes border-connected white (threshold=220 catches
//   off-white/cream paper; transparent pixels treated as background).
// Pass 2: dilate the removed region by JUMP pixels to cross thin colored outlines
//   (drawn borders block the BFS in pass 1), then BFS again to flood the white
//   paper enclosed inside those outlines.
function removeWhiteBg(img, threshold = 220, noDilation = false) {
  const canvas = document.createElement('canvas')
  const W = img.naturalWidth || img.width
  const H = img.naturalHeight || img.height
  canvas.width = W; canvas.height = H
  const ctx = canvas.getContext('2d')
  ctx.drawImage(img, 0, 0)
  const imageData = ctx.getImageData(0, 0, W, H)
  const px = imageData.data

  // For EST illustrations: detect the gray #E8E8E8 background generated by Gemini.
  // White clothing (255,255,255) differs from gray (232,232,232) by ~23 units → stays intact.
  // For children's drawings: standard brightness threshold.
  const GR = 232  // #E8E8E8
  const TOL = 7   // removes 225–239 only; clothing anti-aliased edges (240+) and white (255) kept
  const isBackground = noDilation
    ? (pos) => {
        const i = pos * 4
        if (px[i + 3] < 10) return true
        const r = px[i], g = px[i + 1], b = px[i + 2]
        return Math.abs(r - GR) <= TOL && Math.abs(g - GR) <= TOL && Math.abs(b - GR) <= TOL
      }
    : (pos) => {
        const i = pos * 4
        if (px[i + 3] < 10) return true
        return px[i] > threshold && px[i + 1] > threshold && px[i + 2] > threshold
      }

  const visited = new Uint8Array(W * H)

  const bfsFill = (seeds) => {
    let head = 0
    while (head < seeds.length) {
      const pos = seeds[head++]
      const x = pos % W, y = (pos / W) | 0
      for (const [dx, dy] of [[-1, 0], [1, 0], [0, -1], [0, 1]]) {
        const nx = x + dx, ny = y + dy
        if (nx < 0 || nx >= W || ny < 0 || ny >= H) continue
        const npos = ny * W + nx
        if (!visited[npos] && isBackground(npos)) { visited[npos] = 1; seeds.push(npos) }
      }
    }
  }

  // Pass 1: seed from all four border edges, remove only pixels reachable from border
  const seeds1 = []
  for (let x = 0; x < W; x++) {
    for (const y of [0, H - 1]) {
      const p = y * W + x
      if (!visited[p] && isBackground(p)) { visited[p] = 1; seeds1.push(p) }
    }
  }
  for (let y = 1; y < H - 1; y++) {
    for (const x of [0, W - 1]) {
      const p = y * W + x
      if (!visited[p] && isBackground(p)) { visited[p] = 1; seeds1.push(p) }
    }
  }
  bfsFill(seeds1)

  // Pass 2: dilation to cross thin drawn outlines (children's drawings only)
  if (!noDilation) {
    const JUMP = Math.min(80, Math.max(20, Math.round((W + H) / 60)))
    const dist = new Int16Array(W * H).fill(-1)
    const dq = []
    for (let i = 0; i < W * H; i++) if (visited[i]) { dist[i] = 0; dq.push(i) }
    let dHead = 0
    while (dHead < dq.length) {
      const pos = dq[dHead++]
      if (dist[pos] >= JUMP) continue
      const x = pos % W, y = (pos / W) | 0
      for (const [dx, dy] of [[-1, 0], [1, 0], [0, -1], [0, 1]]) {
        const nx = x + dx, ny = y + dy
        if (nx < 0 || nx >= W || ny < 0 || ny >= H) continue
        const npos = ny * W + nx
        if (dist[npos] < 0) { dist[npos] = dist[pos] + 1; dq.push(npos) }
      }
    }
    const seeds2 = []
    for (let i = 0; i < W * H; i++) {
      if (dist[i] > 0 && !visited[i] && isBackground(i)) { visited[i] = 1; seeds2.push(i) }
    }
    bfsFill(seeds2)

    // Phase 3: remove any remaining enclosed background regions (e.g., counter of 'О').
    // After phases 1–2, any unvisited background pixel is fully enclosed by dark outlines.
    const seeds3 = []
    for (let i = 0; i < W * H; i++) {
      if (!visited[i] && isBackground(i)) { visited[i] = 1; seeds3.push(i) }
    }
    bfsFill(seeds3)
  }

  for (let i = 0; i < W * H; i++) {
    if (visited[i]) px[i * 4 + 3] = 0
  }

  ctx.putImageData(imageData, 0, 0)
  return canvas
}

// ─── Per-mockup colour overrides ─────────────────────────────────────────────

function hexToRgb(hex) {
  return [parseInt(hex.slice(1,3),16), parseInt(hex.slice(3,5),16), parseInt(hex.slice(5,7),16)]
}
function rgbToHex(r,g,b) {
  return '#' + [r,g,b].map(v => v.toString(16).padStart(2,'0')).join('')
}

async function extractDesignColors(imageUrl, maxColors = 24) {
  const img = await loadImgEl(imageUrl)
  const W = img.naturalWidth || img.width, H = img.naturalHeight || img.height
  const canvas = document.createElement('canvas')
  canvas.width = W; canvas.height = H
  const ctx = canvas.getContext('2d')
  ctx.drawImage(img, 0, 0)
  const data = ctx.getImageData(0, 0, W, H).data
  const counts = new Map()
  for (let i = 0; i < data.length; i += 4) {
    if (data[i+3] < 128) continue
    const r = Math.min(255, Math.round(data[i]/8)*8)
    const g = Math.min(255, Math.round(data[i+1]/8)*8)
    const b = Math.min(255, Math.round(data[i+2]/8)*8)
    const k = rgbToHex(r, g, b)
    counts.set(k, (counts.get(k) || 0) + 1)
  }
  return [...counts.entries()].sort((a,b) => b[1]-a[1]).slice(0, maxColors).map(([c]) => c)
}

async function applyMockupColorMap(imageUrl, colorMap) {
  if (!imageUrl || !colorMap) return imageUrl
  const entries = Object.entries(colorMap).filter(([from, to]) => from !== to)
  if (!entries.length) return imageUrl
  const img = await loadImgEl(imageUrl)
  const W = img.naturalWidth || img.width, H = img.naturalHeight || img.height
  const canvas = document.createElement('canvas')
  canvas.width = W; canvas.height = H
  const ctx = canvas.getContext('2d')
  ctx.drawImage(img, 0, 0)
  const iData = ctx.getImageData(0, 0, W, H)
  const d = iData.data
  const pairs = entries.map(([from, to]) => ({ f: hexToRgb(from), t: hexToRgb(to) }))
  const TOL = 60
  for (let i = 0; i < d.length; i += 4) {
    if (d[i+3] < 10) continue
    for (const { f, t } of pairs) {
      if (Math.abs(d[i]-f[0]) + Math.abs(d[i+1]-f[1]) + Math.abs(d[i+2]-f[2]) < TOL) {
        d[i] = t[0]; d[i+1] = t[1]; d[i+2] = t[2]; break
      }
    }
  }
  ctx.putImageData(iData, 0, 0)
  return canvas.toDataURL('image/png')
}

function drawLetters(ctx, letters, W, H, style = 'D') {
  const isTwoColor = style.includes('TWO_COLOR')
  for (const letter of letters) {
    const lx = letter.x / 100 * W
    const ly = letter.y / 100 * H
    const isA = letter.type === 'A'
    const vW = isA ? 1600 : 360, vH = isA ? 1799 : 460
    const lw = letter.size / 100 * W
    const lh = lw * vH / vW
    const sc = lw / vW
    ctx.save()
    ctx.translate(lx + lw / 2, ly + lh / 2)
    ctx.rotate(letter.rotation * Math.PI / 180)
    ctx.translate(-lw / 2, -lh / 2)
    ctx.scale(sc, sc)
    if (letter.type === 'A') {
      // transform: x→1.4x+49, y→1799-y (matches SVG translate(49 1799) scale(1.4 -1))
      ctx.transform(1.4, 0, 0, -1, 49, 1799)
      const aOuter = new Path2D(A_PATH_ORIG)
      const aHole = new Path2D(A_INNER_ORIG)
      if (isTwoColor) {
        ctx.fillStyle = letter.fillColor || '#ffffff'
        ctx.fill(new Path2D(A_PATH_ORIG + ' ' + A_INNER_ORIG), 'evenodd')
      }
      ctx.strokeStyle = letter.color
      ctx.lineWidth = A_STROKE_W
      ctx.stroke(aOuter)
      ctx.stroke(aHole)
    } else if (letter.type === 'Y') {
      if (isTwoColor) {
        ctx.fillStyle = letter.color
        ctx.fill(new Path2D(Y_PATH), 'evenodd')
        ctx.fillStyle = letter.fillColor || '#ffffff'
        ctx.fill(new Path2D(Y_PATH_INNER), 'evenodd')
      } else {
        ctx.fillStyle = letter.color
        ctx.fill(new Path2D(Y_PATH), 'evenodd')
      }
    } else {
      // type D (default)
      ctx.translate(-60, -110)
      if (isTwoColor) {
        ctx.fillStyle = letter.color
        ctx.fill(new Path2D(D_PATH), 'evenodd')
        ctx.fillStyle = letter.fillColor || '#ffffff'
        ctx.fill(new Path2D(D_PATH_INNER), 'evenodd')
      } else {
        ctx.fillStyle = letter.color
        ctx.fill(new Path2D(D_PATH), 'evenodd')
      }
    }
    ctx.restore()
  }
}

